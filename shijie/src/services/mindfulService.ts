// 通用兜底步骤
const FALLBACK_STEPS = [
  '深呼吸三次，放松肩膀',
  '打开你需要用到的工具或页面',
  '做最小的一步，哪怕只是看一眼',
  '告诉自己：开始了就好',
];

/**
 * 根据任务标题和描述生成正念启动步骤
 */
export function generateMindfulSteps(title: string, description?: string): MindfulStep[] {
  const text = `${title} ${description || ''}`.toLowerCase();

  // 匹配关键词模板
  for (const [keywords, steps] of TEMPLATES) {
    if (keywords.some((kw) => text.includes(kw))) {
      return steps.map((text, i) => ({
        id: `step-${i}`,
        text,
        completed: false,
      }));
    }
  }

  // 兜底
  return FALLBACK_STEPS.map((text, i) => ({
    id: `step-${i}`,
    text,
    completed: false,
  }));
}
